class SpamMessage:
    @staticmethod
    def reaction_spam(user_full_name: str):
        return (
            f"🛡 Користувача <b>{user_full_name}</b> заблоковано (Бан).\n"
            f"⛔️ <b>Причина:</b> Reaction Spam.\n"
            f"📉 <i>З метою привернення уваги до профілю (реклама/18+).</i>"
        )

    @staticmethod
    def spam_18(user_full_name: str):
        return f"‼️ Користувач {user_full_name} заблоковано.\nПричина: ⛔️ Спам 18+."

    @staticmethod
    def mute(user_full_name: str):
        return (
            f"🤐 Користувач <b>{user_full_name}</b> тимчасово переведений у режим читання (Мут).\n"
            f"⚠️ <b>Причина:</b> Підозра на спам.\n"
            f"⏳ <i>Обмеження буде знято після ручної перевірки адміністратором.</i>"
        )

    @staticmethod
    def stop_links(u_id: int, user_full_name: str):
        return f'⚠️ <a href="tg://user?id={u_id}">{user_full_name}</a> посилання в цьому чаті заборонені.'

    @staticmethod
    def stop_links_mute(u_id: int, user_full_name: str):
        return f'⚠️ <a href="tg://user?id={u_id}">{user_full_name}</a> посилання в цьому чаті заборонені.\nЧерез повторні порушення ви тимчасово переведені в тихий режим (мут).'

    @staticmethod
    def stop_card_number(u_id: int, user_full_name: str):
        return f'⚠️ <a href="tg://user?id={u_id}">{user_full_name}</a> ваше повідомлення видалено.\nПричина: Номер картки без узгодження з адмінами.'

    @staticmethod
    def spam(user_full_name: str):
        return (
            f"‼️ Користувач {user_full_name} заблоковано.\nПричина: 🚫 Рекламний спам."
        )

    @staticmethod
    def emoji_spam(user_full_name: str):
        return f"‼️ {user_full_name}, повідомлення видалено.\nЗабагато емодзі. Так часто маскують рекламу, тому заради безпеки чату я його прибрав."

    @staticmethod
    def russian_language(user_full_name: str):
        return f"🛡 {user_full_name}, я видалив ваше повідомлення, оскільки в цьому чаті не пишуть російською. "


